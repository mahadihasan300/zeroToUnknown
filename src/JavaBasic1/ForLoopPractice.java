package JavaBasic1;

public class ForLoopPractice {
    public static void main(String[] args) {
        for(int i =0; i< 10; i++){
            System.out.println("Hello");
        }

        int grade[] = {1, 232, 232, 21};
        for(int i =0; i<grade.length; i++){
            System.out.println(grade[i]);
        }
    }
}
